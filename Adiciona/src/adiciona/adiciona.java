package adiciona;

import javax.swing.JFrame;


/**
 *
 * @author vitor.felicio
 */
class adiciona {
    public static void main(String[] args) {
        // Cria uma nova instância do JFrame
        JFrame frame = new JFrame("Meu JFrame");
        
        // Configurações do JFrame
        frame.setSize(500, 500);  // Define o tamanho do JFrame em pixels
        frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);  // Define o comportamento padrão ao fechar o JFrame
        
        // Define a visibilidade do JFrame como verdadeira
        frame.setVisible(true);
        
    }
}
